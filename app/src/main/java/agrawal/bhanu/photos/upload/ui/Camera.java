    @Override
    public void onRequestPermissionsResult(int requestCode, @NonNull String[] permissions, @NonNull int[] grantResults) {
        switch (requestCode) {
            case Constants.CAMERA_PERMISSION:

                if (grantResults.length > 0) {

                    boolean cameraPermission = grantResults[0] == PackageManager.PERMISSION_GRANTED;
                    if (cameraPermission) {
                        goAhead();
                    } else {
                        Toast.makeText(getContext(), "No Camera Permissions ", Toast.LENGTH_SHORT).show();
                    }

                }
                break;

            case Constants.STORAGE_PERMISSION:

                if (grantResults.length > 0) {

                    boolean storagePermission = grantResults[0] == PackageManager.PERMISSION_GRANTED;
                    if (storagePermission) {
                        goAhead();
                    } else {
                        Toast.makeText(getContext(), "No storage Permissions ", Toast.LENGTH_SHORT).show();
                    }

                }
                break;
            case Constants.CAMERA_STORAGE_PERMISSION:

                if (grantResults.length > 0) {

                    boolean cameraPermission = grantResults[0] == PackageManager.PERMISSION_GRANTED;
                    boolean storagePermission = grantResults[1] == PackageManager.PERMISSION_GRANTED;

                    if (cameraPermission &&
                            storagePermission) {
                        goAhead();
                    } else {
                        Toast.makeText(getContext(), "No Camera Permissions ", Toast.LENGTH_SHORT).show();
                    }

                }
                break;
            default:
                super.onRequestPermissionsResult(requestCode, permissions, grantResults);
        }
    }

